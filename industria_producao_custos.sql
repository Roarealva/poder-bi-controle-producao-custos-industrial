-- =========================================================
-- Banco de dados: Controle de Produção e Custos Industrial
-- Cenário fictício inspirado no setor industrial de Bauru/SP
-- =========================================================

CREATE TABLE dim_departamentos (
    cod_departamento VARCHAR(5) PRIMARY KEY,
    nome_departamento VARCHAR(100)
);

CREATE TABLE dim_linhas_producao (
    cod_linha VARCHAR(5) PRIMARY KEY,
    nome_linha VARCHAR(100),
    cod_departamento VARCHAR(5),
    FOREIGN KEY (cod_departamento) REFERENCES dim_departamentos(cod_departamento)
);

CREATE TABLE dim_produtos (
    cod_produto VARCHAR(5) PRIMARY KEY,
    nome_produto VARCHAR(100),
    cod_linha VARCHAR(5),
    custo_unitario DECIMAL(10,2),
    FOREIGN KEY (cod_linha) REFERENCES dim_linhas_producao(cod_linha)
);

CREATE TABLE fato_producao (
    id INT PRIMARY KEY AUTO_INCREMENT,
    data DATE,
    cod_produto VARCHAR(5),
    meta_producao INT,
    qtd_produzida INT,
    qtd_defeituosa INT,
    FOREIGN KEY (cod_produto) REFERENCES dim_produtos(cod_produto)
);

CREATE TABLE fato_custos (
    id INT PRIMARY KEY AUTO_INCREMENT,
    mes_referencia DATE,
    cod_departamento VARCHAR(5),
    orcamento DECIMAL(10,2),
    custo_realizado DECIMAL(10,2),
    FOREIGN KEY (cod_departamento) REFERENCES dim_departamentos(cod_departamento)
);

